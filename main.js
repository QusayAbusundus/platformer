class Hero
{
	constructor(x, y, width, height)
	{
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
		this.xVelocity = 0;
		this.yVelocity = 0;
	}

	move()
	{
	}

	show()
	{
		fill(255);
		ellipse(this.x, this.y, this.width, this.height);
	}
}

class Platform
{
	constructor(x, y, width)
	{
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = 20;
	}

	move()
	{
	}

	show()
	{
		fill(125);
		rect(this.x, this.y, this.width, this.height);
	}
}